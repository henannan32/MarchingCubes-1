static float implicit(float x, float y, float z) {
	return (x - 128) * (x - 128) + (y - 128)*(y - 128) + (z - 128) * (z - 128) - 100 * 100;
}

static void calcVoxelValues(XYZ points[], double vals[]) {
	int i;
	for (i = 0; i < 8; i++) {
		vals[i] = implicit(points[i].x, points[i].y, points[i].z);
	}

}

