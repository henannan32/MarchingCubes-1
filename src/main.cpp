{
	if(key == GLFW_KEY_ESCAPE && action == GLFW_PRESS) {
		glfwSetWindowShouldClose(window, GL_TRUE);
	} else if (key == GLFW_KEY_A && (action == GLFW_PRESS || action == GLFW_REPEAT)) {
		glm::vec3 right(normalize(cross(
			glm::vec3(0, 1, 0),
			glm::vec3(eye - target))));
		eye -= glm::vec3(right.x * 0.2, 0, right.z * 0.2);
		target -= glm::vec3(right.x * 0.2, 0, right.z * 0.2);
	} else if (key == GLFW_KEY_D && (action == GLFW_PRESS || action == GLFW_REPEAT)) {
		glm::vec3 right(normalize(cross(
			glm::vec3(0, 1, 0),
			glm::vec3(eye - target))));
		eye += glm::vec3(right.x * 0.2, 0, right.z * 0.2);
		target += glm::vec3(right.x * 0.2, 0, right.z * 0.2);
	}
	if (key == GLFW_KEY_W && (action == GLFW_PRESS || action == GLFW_REPEAT)) {
		glm::vec3 view(normalize(target - eye));
		eye += glm::vec3(view.x * 0.2, 0, view.z * 0.2);
		target += glm::vec3(view.x * 0.2, 0, view.z * 0.2);
	} else if (key == GLFW_KEY_S && (action == GLFW_PRESS || action == GLFW_REPEAT)) {
		glm::vec3 view(normalize(target - eye));
		eye -= glm::vec3(view.x * 0.2, 0, view.z * 0.2);
		target -= glm::vec3(view.x * 0.2, 0, view.z * 0.2);
	} 
}

static void mouse_callback(GLFWwindow *window, double xpos, double ypos) {
        if (firsttime) {
                xorig = xpos;
                yorig = ypos;
                firsttime = 0;
        } else {
                double dx = xpos - xorig, dy = ypos - yorig, scale = PI / (float) g_width;
                theta += dx * scale;
                phi -= dy * scale;
                phi = phi > DEG_85 ? DEG_85 : phi;
                phi = phi < -DEG_85 ? -DEG_85 : phi;
                xorig = xpos;
                yorig = ypos;
		target = glm::vec3(
			cos(phi) * cos(theta) + eye.x,
			sin(phi) + eye.y,
			cos(phi) * cos(PI / 2.0 - theta) + eye.z);
        }
}
 
/* resize window call back */
static void resize_callback(GLFWwindow *window, int width, int height) {
   g_width = width;
   g_height = height;
   glViewport(0, 0, width, height);
}

int main(int argc, char **argv)
{
	if(argc < 2) {
		cout << "Please specify the resource directory." << endl;
		return 0;
	}
	RESOURCE_DIR = argv[1] + string("/");

	// Set error callback.
	glfwSetErrorCallback(error_callback);
	// Initialize the library.
	if(!glfwInit()) {
		return -1;
	}
   	//request the highest possible version of OGL - important for mac
   	glfwWindowHint(GLFW_OPENGL_FORWARD_COMPAT, GL_TRUE);
   	glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);
   	glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
   	glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 2);

	// Create a windowed mode window and its OpenGL context.
	window = glfwCreateWindow(g_width, g_height, "FBO test", NULL, NULL);
	if(!window) {
		glfwTerminate();
		return -1;
	}
	// Make the window's context current.
	glfwMakeContextCurrent(window);
	// Initialize GLEW.
	glewExperimental = true;
	if(glewInit() != GLEW_OK) {
		cerr << "Failed to initialize GLEW" << endl;
		return -1;
	}
	//weird bootstrap of glGetError
   	glGetError();
	cout << "OpenGL version: " << glGetString(GL_VERSION) << endl;
   	cout << "GLSL version: " << glGetString(GL_SHADING_LANGUAGE_VERSION) << endl;

	// Set vsync.
	glfwSwapInterval(1);
	// Set keyboard callback.
	glfwSetKeyCallback(window, key_callback);
	glfwSetCursorPosCallback(window, mouse_callback);
   	//set the window resize call back
   	glfwSetFramebufferSizeCallback(window, resize_callback);

	// Initialize scene. Note geometry initialized in init now
	fillVertexArray();
	init();
	initGeom();

	// Loop until the user closes the window.
	while(!glfwWindowShouldClose(window)) {
		// Render scene.
		render();
		// Swap front and back buffers.
		glfwSwapBuffers(window);
		// Poll for and process events.
		glfwPollEvents();
	}
	// Quit program.
	glfwDestroyWindow(window);
	glfwTerminate();
	return 0;
}
